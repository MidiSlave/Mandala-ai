/**
 * Pre-converted icon shapes as UV point arrays.
 * Each icon is a set of subpaths (each subpath is a [number, number][] polyline).
 * Coordinates are in [0,1]×[0,1] UV space.
 *
 * Icons are organized by NewsTheme for live mode integration.
 * 3 icons per theme × 16 themes = 48 icons.
 */

export type IconShape = {
    name: string;
    paths: [number, number][][]; // multiple subpaths per icon
};

export type ThemeIcons = {
    [theme: string]: IconShape[];
};

// Helper: scale & center a shape within UV bounds with margin
function scaleShape(
    paths: [number, number][][],
    margin = 0.08
): [number, number][][] {
    // Find bounding box
    let minX = Infinity, minY = Infinity, maxX = -Infinity, maxY = -Infinity;
    for (const path of paths) {
        for (const [x, y] of path) {
            if (x < minX) minX = x;
            if (y < minY) minY = y;
            if (x > maxX) maxX = x;
            if (y > maxY) maxY = y;
        }
    }
    const w = maxX - minX || 1;
    const h = maxY - minY || 1;
    const scale = Math.min((1 - margin * 2) / w, (1 - margin * 2) / h);
    const cx = (minX + maxX) / 2;
    const cy = (minY + maxY) / 2;
    return paths.map(path =>
        path.map(([x, y]) => [
            (x - cx) * scale + 0.5,
            (y - cy) * scale + 0.5
        ] as [number, number])
    );
}

